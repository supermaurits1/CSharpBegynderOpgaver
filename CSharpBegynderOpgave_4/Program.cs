//Eksempel på while loop

//While loop der tæller til 10
int counter = 0;
while(counter < 10)
{
	Console.WriteLine(counter);
	counter++;
}

Console.ReadKey();
Console.WriteLine("");

//Eksempel på for loop
for(int i = 0; i < 10; i += 2)
{
	Console.WriteLine(i);
}
Console.ReadKey();



